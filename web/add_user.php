<?php
require_once("vendor/autoload.php");

use App\Data\User;
use App\Repositories\UserRepository; 
use App\Middlewares\AddUser;
use App\Common\Email;
use App\Common\Phone;
use App\Common\InputValidator;
use App\Common\Username;
use App\Common\Password;

/**
* initialize objects
*/
$user = new User();
$userRepository = new UserRepository();
$email = new Email();
$phone = new Phone();
$username = new Username();
$password = new Password();
$inputValidator = new InputValidator();

/**
* set repository
*/
$addUser = new AddUser($userRepository);

/**
* Data validation
*/

// Firstname validation
$fname = $_POST['fname'];
$fname = $inputValidator->validateInput($fname);

// Lastname validation
$lname = $_POST['lname'];
$lname = $inputValidator->validateInput($lname);

// Email validation
$userEmail = $_POST['email'];
$email->setEmail($userEmail); 
$userEmail = $email->validEmail();

// Phone validation
$userPhone = $_POST['phone'];
$phone->setPhoneNumber($userPhone);
$userPhone = $phone->validPhoneNumber();

// Username autogenerated
$userName = $username->generateUsername();

// Password autogenerated
$userPassword = $password->generatePassword();
$pwd = $userPassword;
$password->setPassword($userPassword);
$userPassword = $password->encryptPassword();

/**
* set data
*/
$user->setFname($fname);
$user->setLname($lname);
$user->setEmail($userEmail);
$user->setPhone($userPhone);
$user->setUsername($userName);
$user->setPassword($userPassword);

$main["content"] = array();
$content["info"] = array();
$content["user"] = array();

if(empty($userEmail) || empty($userPhone)){
	//Validation failed 
	$info["status"] = false;
	$info["records"] = 0;
}else{
	$user = $addUser->createUser($user);

	if($user != null){
		$info["status"] = true;
		$info["records"] = 1;
		$data['user_id'] = $user->getUserId();
		$data['fname'] = $user->getFname();
		$data['username'] = $user->getUsername();
		$data['password'] = $user->getPassword();
	}
}

array_push($content["info"],$info);
array_push($content["user"],$data);
array_push($main["content"],$content);

echo json_encode($main, true);